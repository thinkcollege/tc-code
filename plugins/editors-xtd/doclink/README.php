1. What ?
--------------------------------------------------------------

DOCLink is an Joomla! editor plugin that allows the insertion of
links to DOCman files using a popup dialog.

Features :

	- Insert links to DOCman documents and categories
	- Add file date/size/icon
	- International language support (Language packs) currenly UK
	- Multiple browser support, currently Mozilla, MSIE and FireFox.

NOTE! This version will only work with Joomla! v1.5
Install DOCLink 1.4 if you're using Joomla! v1.0.x

2. Installation
--------------------------------------------------------------

Use Joomla's built in mambot installer.
Don't forget to publish the mambot.


3. Which editors are supported ?
--------------------------------------------------------------

DOCLink supports :

* TinyMCE
* HTMLArea3 XTD
* WysiwygPro
* TMEdit
* mosCE



4. Which browsers are supported ?
---------------------------------------------------------------

